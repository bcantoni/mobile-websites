<h1>About Cantoni.mobi</h1>

<p>Welcome to <strong>Cantoni.mobi</strong>: a high-quality list of mobile websites suitable for
   mobile devices such as the Android, iPhone, Blackberry, Nokida, Windows, or other smartphone.</p>
<p>There are several sites like this on the internet, so what makes <strong>Cantoni.mobi</strong>
   unique?</p>
<ul>
   <li>Sites are chosen based on broad usability and interest</li>
   <li>The list is constantly kept up to date with new sites being added and old 
       ones being cleaned out</li>
   <li>Suggestions from readers ensure that we aren't missing anything</li>
   <li>Page design is intentionally kept simple and lightweight; suitable for
       setting as your mobile browser homepage</li>
</ul>
<p>This site is completely free. I do include some mobile advertising to help offset
   bandwidth costs. The goal is to use as little advertising as possible without
   affecting the overall experience.</p>

<p>I hope you find this project helpful, whether you periodically check in to 
   discover new mobile sites or set it as your mobile home page.</p> 

